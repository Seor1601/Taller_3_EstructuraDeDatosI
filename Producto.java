
public class Producto {

    public int id;
    public String nombre;
    public String categoria;
    public long precio;
    public int stock;

    public Producto(int id, String nombre, String categoria, long precio, int stock) {
        this.id = id;
        this.nombre = nombre;
        this.categoria = categoria;
        this.precio = precio;
        this.stock = stock;
    }

    
}
